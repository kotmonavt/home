#принимаем 3 числа. длины сторон треугольника. вывести периметр и площадь
import math
a = float(input())
b = float(input())
c = float(input())
if (a < b + c) and (c < a+b) and (b < c+a):
    p = (a+b+c)/2
    print('Периметр:', a+b+c)
    print('Площадь', math.sqrt(p*(p-a)*(p-b)*(p-c)))
else:
    print('Такого треугольника не существует')

